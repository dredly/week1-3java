public class MathStuff {
    public static void main(String args[]) {
        double input1 = 15;
        double ans1 = Math.sin(input1);
        System.out.println("sin(15) = " + ans1);
        double ans2 = Math.pow((double) 63, (double) 2.5);
        System.out.println("63 ** 2.5 = " + ans2);
        double ans3 = Math.sqrt((double) 2498);
        System.out.println("Square root of 2498 = " + ans3);
        double randNo = Math.random();
        System.out.println("Here is a random numeber: " + randNo);
        System.out.println(Math.PI);
    }
}
